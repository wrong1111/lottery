					<p>七乐彩采用组合式玩法，从01—30共30个号码中选择7个号码组合为一注投注号码。每注金额人民币2元。</p>


					<p>七乐彩每周销售三期，期号以开奖日界定，按日历年度编排。 </p>



					<p>七乐彩投注方法分为自选号码投注和机选号码投注，投注方式分为单式投注、复式投注、胆拖投注和多倍投注。</p>



					<p>自选号码投注是指由投注者自行选定投注号码的投注；机选号码投注是指由投注机为投注者随机产生投注号码的投注。 </p>



					<p>单式投注是从30个号码中选择7个号码，组合为一注投注号码的投注；复式投注是从30个号码中选择8－16个号码，将每7个号码的组合方式都作为一注投注号码的多注投注；胆拖投注是在30个号码中选择1至6个号码作为每注都有的胆码，再补充其它不同的号码，进行每7个号码为一组的多注投注；多倍投注是指同样的投注号码进行多注投注。
					</p>



					<strong>第三章　设　奖 </strong>

					<p>七乐彩设奖奖金为销售总额的50%，其中当期奖金为销售总额的49%，调节基金为销售总额的1%。
					</p>

					<p>七乐彩当期奖金设七个奖等，一至三等奖为高奖等，四至七等奖为低奖等。高奖等采用浮动设奖，低奖等采用固定设奖。当期奖金减去当期低奖等奖金为当期高奖等奖金，具体设奖如下： </p>

					<p>一等奖：奖金总额为当期高奖等奖金的70%；</p>

					<p>二等奖：奖金总额为当期高奖等奖金的10%；</p>

					<p>三等奖：奖金总额为当期高奖等奖金的20%；</p>

					<p>四等奖：单注奖金额固定为200元；</p>

					<p>五等奖：单注奖金额固定为50元；</p>

					<p>六等奖：单注奖金额固定为10元；</p>

					<p>七等奖：单注奖金额固定为5元。</p>



