					<strong>三、 奖金计算：</strong><br>
					<br>
					　　第一条：单场胜负游戏按销售总额的73％、9％和18％分别计提彩票奖金、彩票发行费和彩票公益金。彩票奖金中，72%为当期奖金，1%为调节基金。<br>
					　　第二条：奖金分配办法如下：单场胜负游戏设置固定奖金。在每个竞猜比赛场次开始销售前，国家体育总局体育彩票管理中心通过专用计算机分析系统为每个比赛结果选项初设固定奖金。该固定奖金为相对固定奖金，其在销售过程中根据投注额和其他相关因素调整变动，购买者在完成某一投注时所获得的对应固定奖金额即为该投注中奖后的奖金额，不受之后调整变动的影响。固定奖金的初设和调整变动情况通过销售系统和其他指定方式予以公布。<br>
					　　第三条：单场胜负游戏根据投注情况设置最高奖金限额，具体为：<br>
					　　　　　　单场投注，单注最高奖金限额10万元；<br>
					　　　　　　2场和3场过关投注，单注最高奖金限额20万元；<br>
					　　　　　　4场和5场过关投注，单注最高奖金限额50万元；<br>
					　　　　　　6场和6场以上过关投注，单注最高奖金限额100万元。<br>
					　　　　　　彩票中奖后，若单注应兑奖金高于对应的最高奖金限额，则只兑付本规则设定的对应最高限额奖金。<br>
					　　第四条：单场胜负游戏设置调节基金。调节基金包括按照销售额1%提取的部分、逾期未退票的票款，专项用于支付各种不可预见的奖金风险支出和开展派奖。<br>
					　　第五条：单场胜负游戏设置奖池。奖池资金由每个竞猜场次彩票的计提奖金与实际中出奖金的差额累计而成。当某个竞猜场次彩票的计提奖金超过其实际中出奖金时，余额进入奖池。当某个竞猜场次彩票的计提奖金小于其实际中出奖金时，差额由奖池资金补足。当奖池资金总额不足时，由调节基金补足；调节基金不足时，用彩票兑奖周转金垫支。在出现彩票兑奖周转金垫支的情况下，当调节基金出现余额后，应当优先偿还垫支的彩票兑奖周转金。<br>
					　　第六条：单场胜负游戏与其他中国篮球彩票单场竞猜游戏共用奖池、调节基金。<br>
					　　第七条：根据法律规定，彩票的奖金收入属于偶然所得，如单注彩票的中奖金额超过10000元（含10000元）人民币，中奖者需要按照规定缴纳中奖金额的20%作为个人所得税。<br>
